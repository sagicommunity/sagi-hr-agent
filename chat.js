export default async function handler(req, res) {
  if (req.method !== 'POST') {
    res.status(405).json({ error: 'Method not allowed' });
    return;
  }
  const apiKey = process.env.ANTHROPIC_API_KEY;
  if (!apiKey) {
    res.status(500).json({ error: 'ANTHROPIC_API_KEY не задан в настройках Vercel (Settings → Environment Variables).' });
    return;
  }
  try {
    let body = req.body;
    if (typeof body === 'string') body = JSON.parse(body);
    const incoming = Array.isArray(body?.messages) ? body.messages : [];
    // sanitize + cap history to last 24 turns
    const messages = incoming
      .filter(m => m && (m.role === 'user' || m.role === 'assistant') && typeof m.content === 'string' && m.content.trim())
      .slice(-24)
      .map(m => ({ role: m.role, content: m.content }));
    if (!messages.length) {
      res.status(400).json({ error: 'Пустой запрос' });
      return;
    }

    const anthRes = await fetch('https://api.anthropic.com/v1/messages', {
      method: 'POST',
      headers: {
        'content-type': 'application/json',
        'x-api-key': apiKey,
        'anthropic-version': '2023-06-01',
      },
      body: JSON.stringify({
        model: 'claude-sonnet-4-6',
        max_tokens: 2000,
        system: SYSTEM_PROMPT,
        messages,
      }),
    });

    const data = await anthRes.json();
    if (!anthRes.ok) {
      const msg = data?.error?.message || ('Anthropic API error ' + anthRes.status);
      res.status(anthRes.status).json({ error: msg });
      return;
    }
    const reply = (data.content || [])
      .filter(b => b.type === 'text')
      .map(b => b.text)
      .join('\n')
      .trim();
    res.status(200).json({ reply });
  } catch (e) {
    res.status(500).json({ error: e.message || 'Server error' });
  }
}
